  def questions_for_user(user) do
    user
    |> Ecto.assoc(:questions)
    |> Repo.all()
  end

  def answers_for_user(user) do
    user
    |> Ecto.assoc(:answers)
    |> Repo.all()
  end

  def data() do
    Dataloader.Ecto.new(Repo, query: &query/2)
  end

  def query(queryable, _params) do
